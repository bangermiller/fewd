
var numberOne = parseInt ( prompt ("Enter a number"),10 );
var numberTwo = parseInt (prompt ("Enter a seconed number"),10);
var operation = prompt ("Enter a calculation operation");


if (operation == "+") {
	console.log (numberOne + numberTwo);
} else if (operation == "-") {
	console.log (numberOne - numberTwo);
} else if (operation == "*") {
	console.log (numberOne * numberTwo);
} else if (operation == "/") {
	console.log (numberOne / numberTwo);
}














